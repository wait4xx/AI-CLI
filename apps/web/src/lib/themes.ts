export const TERMINAL_THEMES = new Map(THEMES.map((t) => [t.id, t]))
export const TERMINAL_THEME_LIST = THEMES
export const DEFAULT_TERMINAL_THEME = 'tokyo-night'

export function getTerminalTheme(id: string): TerminalTheme {
  return TERMINAL_THEMES.get(id) ?? TERMINAL_THEMES.get(DEFAULT_TERMINAL_THEME)!
}

export function toXtermTheme(t: TerminalTheme) {
  return {
    background: t.background,
    foreground: t.foreground,
    cursor: t.cursor,
    cursorAccent: t.cursorAccent,
    selectionBackground: t.selectionBackground,
    black: t.black,
    red: t.red,
    green: t.green,
    yellow: t.yellow,
    blue: t.blue,
    magenta: t.magenta,
    cyan: t.cyan,
    white: t.white,
    brightBlack: t.brightBlack,
    brightRed: t.brightRed,
    brightGreen: t.brightGreen,
    brightYellow: t.brightYellow,
    brightBlue: t.brightBlue,
    brightMagenta: t.brightMagenta,
    brightCyan: t.brightCyan,
    brightWhite: t.brightWhite,
  }
}
